package Variables;

public class Demo4_DIU {
	
	public static void main(String[] args) {
	
	//Step-1:variable Declaration
	
	 String StudentName;        //DataType variableName
	 int StudentRollNo;         //DataType variableRollNo
	 char StudentGrade;         //DataType variableGrade
	 float StudentPercentage;   //DataType variablePercentage 
	 
	//Step-2:variable Initialization
	 
	 StudentName="Rahul";        //variableName="variable information/data";
	 StudentRollNo=20;           //variableRollNo="variable information/data";
	 StudentGrade='A';          //variableGrade="variable information/data";
	 StudentPercentage=70.80f;  //variablePercentage="variable information/data";
	 
	//Step-3:Usage
	 
	 System.out.println("StudentName:"+StudentName);                //Rahul
	 System.out.println("StudentRollNo:"+StudentRollNo);            //20
	 System.out.println("StudentGrade:"+StudentGrade);              //A
	 System.out.println("StudentPercentage:"+StudentPercentage+"%");//70.80
}

}
